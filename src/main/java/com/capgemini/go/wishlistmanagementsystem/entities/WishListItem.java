package com.capgemini.go.wishlistmanagementsystem.entities;

public class WishListItem {
private String productId;
	
    private String id;
	public WishListItem(String userId,String productId) {
		this.userId=userId;
		this.productId=productId;
	    id=userId+"-itemid-"+productId;
	}
	public WishListItem(){
		
		super();
	}

	public String getProductId() {
		return productId;
	}

	public void setProductId(String productId) {
		this.productId = productId;
	}
	
	private String userId;

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	@Override
	public int hashCode() {
		return id.hashCode();
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		WishListItem other = (WishListItem) obj;
		if (id == null) {
			if (other.id != null)
				return false;
		} else if (!id.equals(other.id))
			return false;
		return true;
	}
	
}
